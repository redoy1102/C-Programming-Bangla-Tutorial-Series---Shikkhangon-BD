#include<stdio.h>
void main(){
    int x = 10;

    printf("%d",x);     //10
    printf("\n%d",x++);   //10
    printf("\n%d",x);     //11
    printf("\n%d",--x);   //10
    printf("\n%d",x);     //10
    printf("\n%d",x--);   //10
    printf("\n%d",x);     //9

}


